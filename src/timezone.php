function get_time_zone($country, $region)
{
    global $_time_zones;
    if(!isset($_time_zones[$country])) {
        return FALSE;
    }
    if(is_string($_time_zones[$country])) {
        return $_time_zones[$country];
    }
    return isset($_time_zones[$country][$region]) ?
        $_time_zones[$country][$region] : FALSE;
}
